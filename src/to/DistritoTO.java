
package to;

public class DistritoTO {
    int iddistrito;
    String nombdist;
    String provdist;
    String obsvdist;

    public String getProvdist() {
        return provdist;
    }

    public void setProvdist(String provdist) {
        this.provdist = provdist;
    }

    public int getIddistrito() {
        return iddistrito;
    }
    
    public void setIddistrito(int iddistrito) {
        this.iddistrito = iddistrito;
    }
    
    public String getNombdist() {
        return nombdist;
    }

    public void setNombdist(String nombdist) {
        this.nombdist = nombdist;
    }

    public String getObsvdist() {
        return obsvdist;
    }

    public void setObsvdist(String obsvdist) {
        this.obsvdist = obsvdist;
    }    
    
}
